#include "main.h"
/**
 * op_add - performs addition
 * @a: integer
 * @b: integer
 * Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - performs subtraction
 * @a: integer
 * @b: integer
 * Return: a - b
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - performs multiplication
 * @a: integer
 * @b: integer
 * Return: a x b
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - performs division
 * @a: integer
 * @b: integer
 * Return: a / b
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - performs modulus op.
 * @a: integer
 * @b: integer
 * Return: a % b
 */
int mod(int a, int b)
{
	return (a % b);
}
