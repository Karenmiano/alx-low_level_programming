#include "3-calc.h"
/**
 * op_add - performs addition
 * @a: integer
 * @b: integer
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - performs subtraction
 * @a: integer
 * @b: integer
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - performs multiplication
 * @a: integer
 * @b: integer
 * Return: a x b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - performs division
 * @a: integer
 * @b: integer
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - performs modulus op.
 * @a: integer
 * @b: integer
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
